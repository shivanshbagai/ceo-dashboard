# --- Database Helpers ---

def fetch_table(table: str, columns: str = "*") -> pd.DataFrame:
    """Fetch all rows from a Supabase table via the SDK (REST API)."""
    response = supabase.table(table).select(columns).execute()
    return pd.DataFrame(response.data) if response.data else pd.DataFrame()


def run_chat_sql(sql: str) -> pd.DataFrame:
    """
    Execute an arbitrary SELECT statement generated by the LLM.
    Strategy: pull the referenced tables from Supabase via SDK, load them into
    an in-memory SQLite database, then run the SQL there.
    This avoids any direct PostgreSQL connection requirement.
    """
    # Find every table name referenced after FROM or JOIN
    tables = list(set(re.findall(r'\b(?:FROM|JOIN)\s+(\w+)', sql, re.IGNORECASE)))

    conn = sqlite3.connect(":memory:")
    try:
        for table in tables:
            df = fetch_table(table)
            if not df.empty:
                df.to_sql(table, conn, if_exists="replace", index=False)

        clean = sql.rstrip(";")
        result = pd.read_sql_query(clean, conn)
    finally:
        conn.close()

    return result


# --- LLM Helpers ---

def _call_llm(prompt: str, system: str = "", max_tokens: int = 600) -> str:
    api_key = os.getenv("GEMINI_API_KEY")
    if not api_key:
        raise ValueError("API Key missing.")

    model = os.getenv("OPENROUTER_MODEL", "google/gemini-2.5-flash")
    url = "https://openrouter.ai/api/v1/chat/completions"

    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
        "HTTP-Referer": "http://localhost:8000",
        "X-Title": "CEO Dashboard",
    }

    messages = []
    if system:
        messages.append({"role": "system", "content": system})
    messages.append({"role": "user", "content": prompt})

    payload = {
        "model": model,
        "messages": messages,
        "temperature": 0.2,
        "max_tokens": max_tokens,
    }

    req = urllib.request.Request(
        url,
        data=json.dumps(payload).encode("utf-8"),
        headers=headers,
        method="POST",
    )
    with urllib.request.urlopen(req) as response:
        res_data = json.loads(response.read().decode("utf-8"))
        if "choices" in res_data and len(res_data["choices"]) > 0:
            return res_data["choices"][0]["message"]["content"].strip()
        elif "error" in res_data:
            raise RuntimeError(f"OpenRouter API error: {res_data['error'].get('message')}")
        else:
            raise RuntimeError(f"Unexpected response format: {res_data}")


def clean_sql(sql: str) -> str:
    """Cleans up and auto-corrects table names in the generated SQL statement."""
    cleaned = re.sub(r"\s+", " ", sql).strip().rstrip(";")

    project_cols = ["client_name", "project_name", "partner_lead", "deal_stage", "pipeline_value",
                    "billable_hours", "milestone_status", "blockers_flag", "csat_score", "renewal_intent"]
    employee_cols = ["department", "current_headcount", "active_hirings", "attrition_count"]
    finance_cols  = ["target_revenue", "actual_revenue", "mrr", "arr"]

    if re.search(r"\bfrom\s*$", cleaned, flags=re.IGNORECASE):
        if any(col in cleaned.lower() for col in finance_cols):
            cleaned = re.sub(r"\bfrom\s*$", "FROM finance_sheets", cleaned, flags=re.IGNORECASE)
        elif any(col in cleaned.lower() for col in project_cols):
            cleaned = re.sub(r"\bfrom\s*$", "FROM crm_pipeline_and_projects", cleaned, flags=re.IGNORECASE)
        elif any(col in cleaned.lower() for col in employee_cols):
            cleaned = re.sub(r"\bfrom\s*$", "FROM hrms_data", cleaned, flags=re.IGNORECASE)

    return cleaned + ";"


