@app.get("/api/kpis/latest")
def get_latest_kpis():
    """Returns the top-level KPI strip data."""
    try:
        # Latest finance row
        fin_resp = supabase.table("finance_sheets").select("*").order("month", desc=True).limit(1).execute()
        if not fin_resp.data:
            return {"error": "No financial data"}
        row = fin_resp.data[0]

        target  = float(row["target_revenue"] or 0)
        actual  = float(row["actual_revenue"] or 0)
        mrr     = float(row["mrr"] or 0)
        delta_pct = ((actual - target) / target * 100) if target else 0

        # Average CSAT (exclude 0-score rows)
        csat_resp = supabase.table("crm_pipeline_and_projects").select("csat_score").gt("csat_score", 0).execute()
        scores = [r["csat_score"] for r in (csat_resp.data or []) if r["csat_score"]]
        avg_csat = sum(scores) / len(scores) if scores else 0.0

        # Weighted pipeline
        pipe_resp = supabase.table("crm_pipeline_and_projects").select("deal_stage,pipeline_value").execute()
        weights = {"Closed Won": 1.00, "Negotiation": 0.70, "Proposal": 0.40, "Qualification": 0.20}
        pipeline = sum(
            float(r["pipeline_value"] or 0) * weights.get(r["deal_stage"], 0)
            for r in (pipe_resp.data or [])
            if r["deal_stage"] in weights
        )

        return {
            "month": row["month"],
            "actual_revenue": actual,
            "revenue_delta_pct": round(delta_pct, 1),
            "mrr": mrr,
            "pipeline": round(pipeline, 2),
            "avg_csat": round(avg_csat, 1),
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


